/*
***************************************************
函数名：oled_ShowStr
功能：显示codetab.h中的ASCII字符,有6*8和8*16可选择
入口参数：x,y -- 起始点坐标(x:0~127, y:0~7);
			ch[] -- 要显示的字符串; 
			TextSize -- 字符大小(small:6*8 ; big:8*16)
返回值：无
应用范围：外部调用
备注：无
***************************************************
*/
void oled_ShowStr(unsigned char x, unsigned char y, unsigned char ch[], unsigned char TextSize){
	unsigned char c = 0,i = 0,j = 0;
	switch(TextSize)
	{
		case small:
		{
			while(ch[j] != '\0')
			{
				c = ch[j] - 32;
				if(x > 126)
				{
					x = 0;
					y++;
				}
				oled_SetPos(x,y);
				for(i=0;i<6;i++)
					oled_WriteDat(F6x8[c][i]);
				x += 6;
				j++;
			}
		}break;
		case big:
		{
			while(ch[j] != '\0')
			{
				c = ch[j] - 32;
				if(x > 120)
				{
					x = 0;
					y++;
				}
				oled_SetPos(x,y);
				for(i=0;i<8;i++)
					oled_WriteDat(F8X16[c*16+i]);
				oled_SetPos(x,y+1);
				for(i=0;i<8;i++)
					oled_WriteDat(F8X16[c*16+i+8]);
				x += 8;
				j++;
			}
		}break;
	}
}

