void oled_Fill(unsigned char fill_Data){
	unsigned char m,n;
	for(m=0;m<8;m++)
	{
		oled_WriteCmd(0xb0+m);		//page0-page1
		oled_WriteCmd(0x00);		//低列起始地址
		oled_WriteCmd(0x10);		//高列起始地址
		for(n=0;n<128;n++)
			{
				oled_WriteDat(fill_Data);
			}
	}
}

